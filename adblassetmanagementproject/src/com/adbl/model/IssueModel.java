package com.adbl.model;

public class IssueModel {
	private String issueid;
	private String issuedby;
	private String issuedto;
	private String issuedate;
	private String issueddateen;
	private String issueitemcode;
	private String statusid;
	private String roomno;
	
	
	public String getIssueid() {
		return issueid;
	}
	public void setIssueid(String issueid) {
		this.issueid = issueid;
	}
	public String getIssuedby() {
		return issuedby;
	}
	public void setIssuedby(String issuedby) {
		this.issuedby = issuedby;
	}
	public String getIssuedto() {
		return issuedto;
	}
	public void setIssuedto(String issuedto) {
		this.issuedto = issuedto;
	}
	public String getIssuedate() {
		return issuedate;
	}
	public void setIssuedate(String issuedate) {
		this.issuedate = issuedate;
	}
	public String getIssueddateen() {
		return issueddateen;
	}
	public void setIssueddateen(String issueddateen) {
		this.issueddateen = issueddateen;
	}
	public String getIssueitemcode() {
		return issueitemcode;
	}
	public void setIssueitemcode(String issueitemcode) {
		this.issueitemcode = issueitemcode;
	}
	public String getStatusid() {
		return statusid;
	}
	public void setStatusid(String statusid) {
		this.statusid = statusid;
	}
	public String getRoomno() {
		return roomno;
	}
	public void setRoomno(String roomno) {
		this.roomno = roomno;
	}
	
	
	

}
